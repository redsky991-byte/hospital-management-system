// RTL languages
const RTL_LANGS = ['ar', 'ur'];

// Language names for the selector
const LANGUAGE_NAMES = {
  en: 'English', de: 'Deutsch', fr: 'Français', nl: 'Nederlands',
  ur: 'اردو', hi: 'हिन्दी', ar: 'العربية', zh: '中文', ja: '日本語'
};

function getLang() {
  return localStorage.getItem('hms_language') || 'en';
}

function setLang(code) {
  if (!TRANSLATIONS[code]) return;
  localStorage.setItem('hms_language', code);
  applyLanguage(code);
}

function t(key, fallback) {
  const lang = getLang();
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  return dict[key] || TRANSLATIONS.en[key] || fallback || key;
}

function applyLanguage(lang) {
  lang = lang || getLang();
  const isRTL = RTL_LANGS.includes(lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
  document.body.classList.toggle('rtl', isRTL);
  applyTranslations();
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translated = t(key);
    if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
      el.placeholder = translated;
    } else {
      el.textContent = translated;
    }
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.getAttribute('data-i18n-title'));
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
}

function initI18n() {
  applyLanguage(getLang());
}

// Auto-init on load
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}

window.t = t;
window.getLang = getLang;
window.setLang = setLang;
window.LANGUAGE_NAMES = LANGUAGE_NAMES;
window.TRANSLATIONS = TRANSLATIONS;
window.applyTranslations = applyTranslations;
